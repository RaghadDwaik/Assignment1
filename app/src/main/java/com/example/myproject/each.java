package com.example.myproject;

public class each {
    private int number ;
    private double price;
    private int floor ;
    private int image;

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getFloor() {
        return floor;
    }

    public void setFloor(int floor) {
        this.floor = floor;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }




    public each(int number, double price, int floor, int image) {
        this.number = number;
        this.price = price;
        this.floor = floor;
        this.image = image;
    }





}
